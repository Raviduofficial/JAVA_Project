    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String username_new =  username.getText();
        String password_new = new String(password.getPassword());
         
        String role = authenticate(username_new,password_new);
        
        if (role != null) {
                dispose(); // Close login window
                openRoleWindow(role);
            } else {
                JOptionPane.showMessageDialog(null, "Invalid credentials!");
            }
    }//GEN-LAST:event_loginActionPerformed
    private String authenticate(String username, String password) {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/user_management", "root", "");
            String query = "SELECT * FROM users WHERE username=? AND password=?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                return rs.getString("role");  // Return the role from the database
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;  // Return null if authentication fails
    }
