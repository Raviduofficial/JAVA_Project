    private void openRoleWindow(String role) {
        // Based on the role, open the appropriate window
        switch (role) {
            case "Admin":
                //new AdminWindow(); // Open Admin window
                //JOptionPane.showMessageDialog(null, "Hello admin");
                admin add = new admin();
                add.setVisible(true);
                break;
            case "Lecturer":
               // new LecturerWindow(); // Open Lecturer window
               //JOptionPane.showMessageDialog(null, "Hello admin");
                lecture lec = new lecture ();
                lec.setVisible(true);
                
                break;
            case "Undergraduate":
                //new UndergraduateWindow(); // Open Undergraduate window
                //JOptionPane.showMessageDialog(null, "Hello boiya");
                undergraduate ug = new undergraduate();
                ug.setVisible(true);
                break;
            case "To":
                //new UndergraduateWindow(); // Open Undergraduate window
                //JOptionPane.showMessageDialog(null, "Hello boiya");
                to t = new to();
                t.setVisible(true);
                break;    
            default:
                JOptionPane.showMessageDialog(null, "Role not found!");
        }
    }
